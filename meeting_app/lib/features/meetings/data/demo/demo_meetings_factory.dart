import '../../domain/entities/meeting.dart';

/// Demo meetings across the Boss portfolio.
abstract final class DemoMeetingsFactory {
  static List<Meeting> build({required DateTime now}) {
    DateTime at(int dayOffset, int hour, [int minute = 0]) => DateTime(
      now.year,
      now.month,
      now.day,
    ).add(Duration(days: dayOffset, hours: hour, minutes: minute));

    Meeting m({
      required String id,
      required String companyId,
      required String title,
      required String description,
      required DateTime startAt,
      required MeetingPriority priority,
      MeetingType type = MeetingType.internal,
      MeetingStatus status = MeetingStatus.scheduled,
      String? location,
      String? meetLink,
      bool isTeamMeeting = false,
      String agenda = '',
      List<String> attachments = const [],
      List<String> actionItems = const [],
      int durationHours = 1,
      int updatedHoursAgo = 2,
      String organizerSuffix = 'ea',
      String organizerLabel = 'Executive Assistant',
      List<String> participantSuffixes = const ['mgr1', 'tl1', 'e1'],
    }) {
      final prefix = companyId.replaceFirst('co_', '');
      final organizerId = 'emp_${prefix}_$organizerSuffix';
      final participants = participantSuffixes
          .map(
            (suffix) => MeetingParticipant(
              userId: 'emp_${prefix}_$suffix',
              name: switch (suffix) {
                'mgr1' => 'Operations Manager',
                'mgr2' => 'Sales Manager',
                'tl1' => 'Operations Lead',
                'tl2' => 'Sales Lead',
                'e1' => 'Associate A',
                'e2' => 'Associate B',
                _ => 'Team member',
              },
            ),
          )
          .toList();
      return Meeting(
        id: id,
        companyId: companyId,
        title: title,
        organizerId: organizerId,
        organizerName: organizerLabel,
        agenda: agenda.isEmpty ? description : agenda,
        description: description,
        priority: priority,
        status: status,
        type: type,
        participants: participants,
        startAt: startAt,
        endAt: startAt.add(Duration(hours: durationHours)),
        meetLink: meetLink ?? 'https://meet.google.com/demo-$id',
        location: location,
        reminderMinutes: 15,
        attachments: attachments,
        notes: '',
        actionItems: actionItems,
        isTeamMeeting: isTeamMeeting,
        teamLeadId: isTeamMeeting ? 'emp_${prefix}_tl1' : null,
        createdAt: now.subtract(Duration(hours: updatedHoursAgo + 24)),
        updatedAt: now.subtract(Duration(hours: updatedHoursAgo)),
      );
    }

    const apex = 'co_apex';
